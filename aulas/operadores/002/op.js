let n = prompt('Digite um número: ')
let m = prompt('Digite outro número: ')

m = parseInt(m)
n = parseInt(n)

document.write('A soma de ' + n + ' e ' + m + ' é: '+ (n + m) + '<br/>') 
document.write('A subtração de ' + n + ' e ' + m + ' é: ' + (n - m) + '<br/>')
document.write('A multiplicação de ' + n + ' e ' + m + ' é: ' + (n * m) + '<br/>')
document.write('A divisão de ' + n + ' e ' + m + ' é: ' + (n / m) + '<br/>')
document.write('A divisão de ' + n + ' e ' + m + ' é: ' + (n % m) + '<br/>')
document.write('O incremento de ' + n + ' é: ' + (++n) + '<br/>')
document.write('O incremento de ' + n + ' é: ' + (n++) + '<br/>')//pos = so aplica após a executação.
document.write('Agora seu número primeiro número é ' + n + '<br/>')
document.write('O decremento de ' + m + ' é: ' + (--m) + '<br/>')
document.write('O decremento de ' + m + ' é: ' + (m--) + '<br/>')
document.write('Agora seu segundo número é: ' + m + '<br/>')







